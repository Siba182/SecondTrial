public class AddTwoNumbers {

    public static void main(String[] args) {

        int num1 = 5;
        int num2 = 15;
        int sum = num1 + num2;

        System.out.println("Sum of these numbers is: " +sum);

        System.out.println("We are now subracting");

        int num3 = 5;
        int sum2 = sum - num3;
        System.out.println(sum + "-" + num3 + "=" + sum2);
    }
}
